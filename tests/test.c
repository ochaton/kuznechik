#include "kuznechik.h"
#include <assert.h> // for assert
#include <string.h> // for memcpy
#include <stdio.h>

void xd(uint8_t *p, ssize_t size) {
	for (uint8_t * c = p; c < p + size; c++) {
		printf("0x%02X ", *c);
	}
	printf("\n");
}


void test_S() {
	fprintf(stderr, "Testing R:\n");
	vect_t in = {
		0xff, 0xee, 0xdd, 0xcc,
		0xbb, 0xaa, 0x99, 0x88,
		0x11, 0x22, 0x33, 0x44,
		0x55, 0x66, 0x77, 0x00,
	};

	vect_t out = {};
	vect_t exp = {
		0xb6, 0x6c, 0xd8, 0x88,
		0x7d, 0x38, 0xe8, 0xd7,
		0x77, 0x65, 0xae, 0xea,
		0x0c, 0x9a, 0x7e, 0xfc,
	};

	memcpy(out.b, in.b, sizeof(in));
	GOST_Kuz_S(&out);

	xd((uint8_t *) out.b, sizeof(out));
	xd((uint8_t *) exp.b, sizeof(exp));
	assert(0 == memcmp(out.b, exp.b, sizeof(out)));

	GOST_Kuz_S_reverse(&out);
	xd((uint8_t *) out.b, sizeof(out));
	xd((uint8_t *) in.b, sizeof(in));

	assert(0 == memcmp(out.b, in.b, sizeof(out)));
}

